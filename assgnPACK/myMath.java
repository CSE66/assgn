package btech.arithmetic;

public class myMath {
    int a,b;
    public myMath(int x,int y){
        a=x;b=y;
    }    
    public int add(){
        return a+b;
    }
    public int sub(){
        return a-b;
    }
    public int multiplication (){
        return a*b;
    }
    public int division(){
        return a/b;
    }
    public int mod(){
        return a%b;
    }
}
